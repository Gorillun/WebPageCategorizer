      public static String filterWords(String word)
      {
          String filtered=word;
          String noGos="public,updated,videos,photos,content,copyright,server,value,function,access,allow,service,status,enable,cookie,cookies,return,check,guide,estimate,privacy,compare,payments,english,espaÃ±ol,terms,policy,mobile,financial,services,signature,posts,community,newsroom,other,media,registration,featured,foundation,center,region,e-news,follow,true,change,custom,downloads,catalog,product,price,lists,contact,video,support,register,visitor,owner,loading,accessories,orders,shipping,ship,header,through,date,watch,post,catch,their,product,them,review,connect,google,twitter,instagram,facebook,youtube,about,create,parts,contact,email,results,what's,more,from,object,send,links,link,next,object ";
          String[] noGo=noGos.split(",");
          int i =0;
          while(i<noGo.length)
          {
              if(filtered.equalsIgnoreCase(noGo[i]))
              {
                  filtered="fail";
                  ++i;
              }
              ++i;
          }
              if(filtered.contains(",")||filtered.contains("_")||filtered.contains("|")||filtered.contains(":")||filtered.contains(")")||filtered.contains("(")||filtered.contains("."))
              {
                  filtered ="fail";
              }
              else if(filtered.contains("=")||filtered.contains(";")||filtered.contains("?")||filtered.contains("$")||filtered.contains("%")||filtered.contains("&")||filtered.contains("!"))
              {
                  filtered ="fail";
              }
               
          
          return filtered;
      }
